public class Relational {
    public static void main(String[] args) {
        int numOne  = 10;
        int numTwo = 20;

        System.out.println(numOne != numTwo);

        System.out.println(numOne == numTwo);

        System.out.println(numOne > numTwo);

        System.out.println(numOne < numTwo);
    }
}
